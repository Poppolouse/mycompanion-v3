import { useState, useEffect, useRef } from 'react';
import './SmartSearch.css';

/**
 * SmartSearch - Geli≈ümi≈ü arama componenti
 * Fuzzy search, AI suggestions, multi-field search √∂zellikleri
 */
function SmartSearch({ 
  games = [], 
  onSearch, 
  placeholder = "Oyun ara...",
  showSuggestions = true,
  showFilters = true 
}) {
  // State'ler
  const [searchTerm, setSearchTerm] = useState('');
  const [suggestions, setSuggestions] = useState([]);
  const [showSuggestionsList, setShowSuggestionsList] = useState(false);
  const [selectedSuggestionIndex, setSelectedSuggestionIndex] = useState(-1);
  const [recentSearches, setRecentSearches] = useState([]);
  const [searchHistory, setSearchHistory] = useState([]);

  // Refs
  const searchInputRef = useRef(null);
  const suggestionsRef = useRef(null);

  // Component mount olduƒüunda localStorage'dan ge√ßmi≈ü aramalarƒ± y√ºkle
  useEffect(() => {
    const savedSearches = localStorage.getItem('gameTracker_searchHistory');
    if (savedSearches) {
      try {
        setSearchHistory(JSON.parse(savedSearches));
      } catch (err) {
        console.error('Arama ge√ßmi≈üi y√ºklenemedi:', err);
      }
    }
  }, []);

  // Arama terimi deƒüi≈ütiƒüinde suggestions g√ºncelle
  useEffect(() => {
    if (searchTerm.length > 0) {
      generateSuggestions(searchTerm);
      setShowSuggestionsList(true);
    } else {
      setSuggestions([]);
      setShowSuggestionsList(false);
    }
    setSelectedSuggestionIndex(-1);
  }, [searchTerm, games]);

  // Fuzzy search algoritmasƒ±
  const fuzzySearch = (text, pattern) => {
    const textLower = text.toLowerCase();
    const patternLower = pattern.toLowerCase();
    
    // Exact match
    if (textLower.includes(patternLower)) {
      return { score: 100, type: 'exact' };
    }
    
    // Fuzzy match
    let score = 0;
    let textIndex = 0;
    
    for (let i = 0; i < patternLower.length; i++) {
      const char = patternLower[i];
      const foundIndex = textLower.indexOf(char, textIndex);
      
      if (foundIndex === -1) {
        return { score: 0, type: 'no-match' };
      }
      
      // Yakƒ±n karakterler daha y√ºksek puan
      const distance = foundIndex - textIndex;
      score += Math.max(0, 10 - distance);
      textIndex = foundIndex + 1;
    }
    
    // Uzunluk farkƒ± cezasƒ±
    const lengthPenalty = Math.abs(text.length - pattern.length) * 0.5;
    score = Math.max(0, score - lengthPenalty);
    
    return { score, type: 'fuzzy' };
  };

  // AI-powered suggestions olu≈ütur
  const generateSuggestions = (term) => {
    if (!term || term.length < 1) {
      setSuggestions([]);
      return;
    }

    const allSuggestions = [];

    // 1. Oyun isimleri
    games.forEach(game => {
      const title = game.title || game.name || '';
      const match = fuzzySearch(title, term);
      
      if (match.score > 20) {
        allSuggestions.push({
          type: 'game',
          text: title,
          score: match.score,
          matchType: match.type,
          game: game,
          icon: 'üéÆ',
          category: 'Oyunlar'
        });
      }
    });

    // 2. Platform √∂nerileri
    const platforms = [...new Set(games.map(g => g.platform || g.sistem).filter(Boolean))];
    platforms.forEach(platform => {
      const match = fuzzySearch(platform, term);
      if (match.score > 30) {
        allSuggestions.push({
          type: 'platform',
          text: platform,
          score: match.score + 10, // Platform'lara bonus
          matchType: match.type,
          icon: 'üñ•Ô∏è',
          category: 'Platformlar'
        });
      }
    });

    // 3. T√ºr √∂nerileri
    const genres = [...new Set(games.map(g => g.genre || g.tur).filter(Boolean))];
    genres.forEach(genre => {
      const match = fuzzySearch(genre, term);
      if (match.score > 30) {
        allSuggestions.push({
          type: 'genre',
          text: genre,
          score: match.score + 5,
          matchType: match.type,
          icon: 'üè∑Ô∏è',
          category: 'T√ºrler'
        });
      }
    });

    // 4. Geli≈ütirici √∂nerileri
    const developers = [...new Set(games.map(g => g.developer || g.gelistirici).filter(Boolean))];
    developers.forEach(developer => {
      const match = fuzzySearch(developer, term);
      if (match.score > 30) {
        allSuggestions.push({
          type: 'developer',
          text: developer,
          score: match.score,
          matchType: match.type,
          icon: 'üë®‚Äçüíª',
          category: 'Geli≈ütiriciler'
        });
      }
    });

    // 5. Durum √∂nerileri
    const statusMap = {
      'oynuyor': 'playing',
      'playing': 'playing',
      'tamamlandƒ±': 'completed',
      'completed': 'completed',
      'durduruldu': 'paused',
      'paused': 'paused',
      'ba≈ülanmadƒ±': 'not-started',
      'not-started': 'not-started'
    };

    Object.keys(statusMap).forEach(statusText => {
      const match = fuzzySearch(statusText, term);
      if (match.score > 40) {
        allSuggestions.push({
          type: 'status',
          text: statusText,
          score: match.score + 15,
          matchType: match.type,
          value: statusMap[statusText],
          icon: getStatusIcon(statusMap[statusText]),
          category: 'Durumlar'
        });
      }
    });

    // 6. Arama ge√ßmi≈üi
    searchHistory.forEach(historyItem => {
      const match = fuzzySearch(historyItem, term);
      if (match.score > 50) {
        allSuggestions.push({
          type: 'history',
          text: historyItem,
          score: match.score - 20, // Ge√ßmi≈ü aramalar daha d√º≈ü√ºk √∂ncelik
          matchType: match.type,
          icon: 'üïí',
          category: 'Ge√ßmi≈ü Aramalar'
        });
      }
    });

    // Skorlara g√∂re sƒ±rala ve en iyi 8'ini al
    const sortedSuggestions = allSuggestions
      .sort((a, b) => b.score - a.score)
      .slice(0, 8);

    setSuggestions(sortedSuggestions);
  };

  // Durum ikonu getir
  const getStatusIcon = (status) => {
    switch (status) {
      case 'playing': return '‚ñ∂Ô∏è';
      case 'completed': return '‚úÖ';
      case 'paused': return '‚è∏Ô∏è';
      case 'not-started': return '‚≠ï';
      default: return 'üéÆ';
    }
  };

  // Arama i≈ülemi
  const handleSearch = (term = searchTerm) => {
    if (term.trim()) {
      // Arama ge√ßmi≈üine ekle
      const newHistory = [term, ...searchHistory.filter(h => h !== term)].slice(0, 10);
      setSearchHistory(newHistory);
      localStorage.setItem('gameTracker_searchHistory', JSON.stringify(newHistory));
      
      // Parent component'e bildir
      onSearch(term);
      setShowSuggestionsList(false);
    }
  };

  // Suggestion se√ßimi
  const handleSuggestionSelect = (suggestion) => {
    let searchValue = '';
    
    switch (suggestion.type) {
      case 'game':
        searchValue = suggestion.text;
        break;
      case 'platform':
        searchValue = `platform:${suggestion.text}`;
        break;
      case 'genre':
        searchValue = `genre:${suggestion.text}`;
        break;
      case 'developer':
        searchValue = `developer:${suggestion.text}`;
        break;
      case 'status':
        searchValue = `status:${suggestion.value}`;
        break;
      case 'history':
        searchValue = suggestion.text;
        break;
      default:
        searchValue = suggestion.text;
    }
    
    setSearchTerm(searchValue);
    handleSearch(searchValue);
  };

  // Klavye navigasyonu
  const handleKeyDown = (e) => {
    if (!showSuggestionsList || suggestions.length === 0) return;

    switch (e.key) {
      case 'ArrowDown':
        e.preventDefault();
        setSelectedSuggestionIndex(prev => 
          prev < suggestions.length - 1 ? prev + 1 : 0
        );
        break;
      
      case 'ArrowUp':
        e.preventDefault();
        setSelectedSuggestionIndex(prev => 
          prev > 0 ? prev - 1 : suggestions.length - 1
        );
        break;
      
      case 'Enter':
        e.preventDefault();
        if (selectedSuggestionIndex >= 0) {
          handleSuggestionSelect(suggestions[selectedSuggestionIndex]);
        } else {
          handleSearch();
        }
        break;
      
      case 'Escape':
        setShowSuggestionsList(false);
        setSelectedSuggestionIndex(-1);
        break;
    }
  };

  // Dƒ±≈üarƒ± tƒ±klama ile suggestions'ƒ± kapat
  useEffect(() => {
    const handleClickOutside = (event) => {
      if (suggestionsRef.current && !suggestionsRef.current.contains(event.target)) {
        setShowSuggestionsList(false);
      }
    };

    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, []);

  // Arama temizle
  const clearSearch = () => {
    setSearchTerm('');
    onSearch('');
    setShowSuggestionsList(false);
    searchInputRef.current?.focus();
  };

  return (
    <div className="smart-search" ref={suggestionsRef}>
      <div className="smart-search__input-container">
        <div className="smart-search__input-wrapper">
          <span className="smart-search__icon">üîç</span>
          <input
            ref={searchInputRef}
            type="text"
            className="smart-search__input"
            placeholder={placeholder}
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            onKeyDown={handleKeyDown}
            onFocus={() => searchTerm && setShowSuggestionsList(true)}
          />
          {searchTerm && (
            <button 
              className="smart-search__clear"
              onClick={clearSearch}
              title="Temizle"
            >
              ‚úï
            </button>
          )}
        </div>
        
        <button 
          className="smart-search__submit"
          onClick={() => handleSearch()}
          title="Ara"
        >
          Ara
        </button>
      </div>

      {/* AI Suggestions */}
      {showSuggestionsList && suggestions.length > 0 && (
        <div className="smart-search__suggestions">
          <div className="smart-search__suggestions-header">
            <span>üí° Akƒ±llƒ± √ñneriler</span>
            <span className="smart-search__suggestions-count">
              {suggestions.length} √∂neri
            </span>
          </div>
          
          <div className="smart-search__suggestions-list">
            {suggestions.map((suggestion, index) => (
              <div
                key={`${suggestion.type}-${index}`}
                className={`smart-search__suggestion ${
                  index === selectedSuggestionIndex ? 'smart-search__suggestion--selected' : ''
                } smart-search__suggestion--${suggestion.matchType}`}
                onClick={() => handleSuggestionSelect(suggestion)}
              >
                <span className="smart-search__suggestion-icon">
                  {suggestion.icon}
                </span>
                <div className="smart-search__suggestion-content">
                  <span className="smart-search__suggestion-text">
                    {suggestion.text}
                  </span>
                  <span className="smart-search__suggestion-category">
                    {suggestion.category}
                  </span>
                </div>
                <span className="smart-search__suggestion-score">
                  {Math.round(suggestion.score)}%
                </span>
              </div>
            ))}
          </div>
          
          <div className="smart-search__suggestions-footer">
            <span>‚Üë‚Üì Gezin ‚Ä¢ Enter Se√ß ‚Ä¢ Esc Kapat</span>
          </div>
        </div>
      )}

      {/* Quick Filters */}
      {showFilters && (
        <div className="smart-search__quick-filters">
          <span className="smart-search__filters-label">Hƒ±zlƒ± Filtreler:</span>
          <button 
            className="smart-search__filter-btn"
            onClick={() => handleSearch('status:playing')}
          >
            ‚ñ∂Ô∏è Oynuyor
          </button>
          <button 
            className="smart-search__filter-btn"
            onClick={() => handleSearch('status:completed')}
          >
            ‚úÖ Tamamlandƒ±
          </button>
          <button 
            className="smart-search__filter-btn"
            onClick={() => handleSearch('platform:PC')}
          >
            üñ•Ô∏è PC
          </button>
          <button 
            className="smart-search__filter-btn"
            onClick={() => handleSearch('genre:RPG')}
          >
            üè∑Ô∏è RPG
          </button>
        </div>
      )}
    </div>
  );
}

export default SmartSearch;